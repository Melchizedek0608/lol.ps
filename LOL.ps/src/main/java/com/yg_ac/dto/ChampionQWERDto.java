package com.yg_ac.dto;

public class ChampionQWERDto {
	private String q;
	private String w;
	private String e;
	private String r;
	
	public ChampionQWERDto() {
		
	}

	public ChampionQWERDto(String q, String w, String e, String r) {
		this.q = q;
		this.w = w;
		this.e = e;
		this.r = r;
	}

	public String getQ() {
		return q;
	}

	public void setQ(String q) {
		this.q = q;
	}

	public String getW() {
		return w;
	}

	public void setW(String w) {
		this.w = w;
	}

	public String getE() {
		return e;
	}

	public void setE(String e) {
		this.e = e;
	}

	public String getR() {
		return r;
	}

	public void setR(String r) {
		this.r = r;
	}

	
}
